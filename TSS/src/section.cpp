//////////////////////////////////////////////////////////////
/// \brief Section::Section
/// Description: Constructor
//////////////////////////////////////////////////////////////
Section::Section()
{
	m_node = "";
	m_numOfConns = -1;
	m_conn1 = "";
	m_conn2 = "";
	m_conn3 = "";
	m_conn4 = "";
	m_ltnum = -1;
	m_thrown = true;		// true for left; false for right
	m_occupancy = false;
	m_thrownLeft = true;
    m_shortSection = false;
}

//////////////////////////////////////////////////////////////
/// \brief Section::Section
/// \param Node
/// \param NumOfConns
/// \param Conn1
/// \param Conn2
/// \param Conn3
/// \param Conn4
/// \param ltNum
/// \param thrownLeft
/// \param shortSection
/// Description: Constructor
//////////////////////////////////////////////////////////////
Section::Section(QString Node, int NumOfConns,
    QString Conn1, QString Conn2, QString Conn3, QString Conn4, int ltNum, bool thrownLeft, bool shortSection)
{
	m_node = Node;
	m_numOfConns = NumOfConns;
	m_conn1 = Conn1;
	m_conn2 = Conn2;
	m_conn3 = Conn3;
	m_conn4 = Conn4;
	m_thrown = true;
	m_occupancy = false;
	m_ltnum = ltNum;
	m_thrownLeft = thrownLeft;
    m_shortSection = shortSection;
}

//////////////////////////////////////////////////////////////
/// \brief Section::~Section
/// Description: Destructor
//////////////////////////////////////////////////////////////
Section::~Section()
{

}

